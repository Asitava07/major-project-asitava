In C programming, arrays are a sequential collection of elements of the same data type. Here's a description of arrays in C in 10 lines:

1. Arrays in C are declared by specifying the data type of elements they hold, followed by the array name and its size in square brackets.
2. The size of an array determines the number of elements it can hold, and it must be a positive integer.
3. Elements in an array are accessed using indices starting from 0 to size-1.
4. Array elements can be of any data type, including integers, characters, floating-point numbers, or even user-defined structures.
5. Arrays can be initialized at the time of declaration by enclosing the initial values within curly braces {}.
6. The individual elements of an array can be accessed and modified using the array name followed by the index in square brackets.
7. Arrays can be passed to functions as arguments, either by value or by reference.
8. C does not perform array bounds checking, so it's essential to ensure that indices stay within the valid range.
9. Arrays can be multi-dimensional, allowing for the creation of matrices and other complex data structures.
